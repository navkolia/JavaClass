package com.syntax.class01;

public class PrimitiveDataTypes {

	public static void main(String[] args) {
		
		// 8 primitive datatypes 
		
		// to store whole numbers --> byte, short, int, long
		byte one = 10;
		short two = 100;
		int three = 1000;
		long six = 1000000L; 
		
		// to store decimal number --> float, double 
		float example = 12.99f;
		double anyEx = 132.87;
		
		// to store single character --> char 
		char singleValue = 'F';
	    char otherValue = '$';
	    
	    
		// to stroe boolean values --> boolean 
		boolean hungry = true; 
		boolean thirsty = false;
		
		System.out.println(singleValue); 
		
		
		
	
}
}