public class TempCheck {

	public static void main(String[] args) {
		
		int temp = 50; 
		int freezeTemp = 32; 
		
		
		
	if(temp < freezeTemp) 
		{
			System.out.println("water will freeze with temperature " + temp);
		}
	else 
		{
			System.out.println("water will not freeze with temperature " + temp);
		}

	}
}
