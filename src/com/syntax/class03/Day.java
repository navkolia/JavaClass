package com.syntax.class03;

public class Day {

	public static void main(String[] args) {
		
		int day = 5; 
		
	if (day == 1)
	{ System.out.println("Monday, No class"); }
	
	else if(day == 2) 
	{ System.out.println("Tuesday, We have class"); } 
	
	else if (day == 3) 
	{ System.out.println("Wednesday, We have Class"); }
	
	else if (day == 4) 
	{ System.out.println("Thursday, We have Class"); }
	
	else if (day == 5) 
	{ System.out.println("Friday, No Class"); }
	
	else if (day == 6) 
	{ System.out.println("Saturday, We have Class"); }
	
	else if (day == 7) 
	{ System.out.println("Sunday, We have Class"); }
	
	else 
	{ System.out.println("No Day"); }
		
	}

}
