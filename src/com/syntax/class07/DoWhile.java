package com.syntax.class07;

public class DoWhile {

	public static void main(String[] args) {
		// say hello 5x 
		// while, check condition first then execute code
		
		int num =6; 
		
		while(num <=5)
		{
			System.out.println("hello"); 
			num++;
		}
		
		System.out.println("XXXXXXXXXXXXXXXXXXXXXX"); 
		
		//do loop, execute 1st then check condition
		
		int num1 =6; 
		do
		{
			System.out.println("Hello");
			num1++; 
		}
			
		while(num1<=5);
		
	}

}
