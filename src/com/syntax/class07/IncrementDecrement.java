package com.syntax.class07;

public class IncrementDecrement {

	public static void main(String[] args) {
		
		int x = 10; 
		x+= 1;  //11
		System.out.println(x);
		x++;   //12
		System.out.println(x);
		
		int y=90; 
		y-=2;  //88
		System.out.println(y);
		y--;  //87
		System.out.println(y);
		
	}

}
